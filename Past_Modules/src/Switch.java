public class Switch {
    public static void main(String[] args) {
        int a = 2;

        switch(a) {
            case 1:
                System.out.println("a는 1입니다");
            case 2:
                System.out.println("a는 2입니다");
            case 3:
                System.out.println("a는 3입니다");
                break;
            default:
                System.out.println("a는 5입니다");
        }
    }
}
