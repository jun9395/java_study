public class SoloOperation {
    public static void main(String [] args) {
        boolean istrue = false;
        int a = 75;

        System.out.println("!istrue : " + !istrue);
        System.out.println("~a : " + ~a);   // 2의 보수
        System.out.println("++a : " + ++a);
        System.out.println("(char)a : " + (char)a);
    }
}
