public class StringSlice {
    public static void main(String[] args) {
        String arr = "0123456789";

        System.out.println("arr : " + arr);
        System.out.println("substring from 5 : " + arr.substring(5));
        System.out.println("substring from 3 to 7 : " + arr.substring(3, 7));
        System.out.println("arr : " + arr);
    }
}
