public class Variable {
    public static void main(String[] args) {
        boolean bool1 = true;
        boolean bool2 = false;

        System.out.println("Bool1: " + bool1);
        System.out.println("Bool2: " + bool2);

        char char1 = 'A';
        char char2 = 'b';

        System.out.println("char1: " + char1 + "\nchar2: " + char2);

        byte mybyte = 127;
        int num1 = 250;
        double comp = 2.48;

        System.out.println("Byte: " + mybyte + "\nint: " + num1 + "\ndouble: " + comp);
    }
}
